import type { Genre, EventChoiceTemplate, EventSeverity, ProductionExecutionImpact, ProductionRole } from '../types';

// Templates for randomized production events. The engine picks a handful of
// these per shoot, biased by an overall risk score, then rolls a concrete
// delta within each range. Ranges are intentionally modest so no single
// event can single-handedly sink or save a film.
//
// Buzz is deliberately absent from most templates: something the public
// could never see (a great take in dailies, an internal VFX review, a
// smooth department meeting) has no business moving pre-release hype, no
// matter how good it feels on set. Buzz only shows up on events with an
// actual public angle - a leak, a paparazzi moment, a stunt visible enough
// to make local news, trade-press chatter about a departure or financing
// trouble. See docs/DESIGN.md 5.x for the full reasoning.
//
// `delayDaysRange` is a real, consumed mechanic (see
// engine/production.ts:rollDayEvent and state/studioReducer.ts) - extra
// shoot days this event actually costs, on top of the day it happened on.
// Always >= 0: a positive event happening doesn't retroactively un-shoot a
// day, so only negative-polarity templates carry a nonzero range.
//
// `severity` (types/index.ts:EventSeverity) is how big a deal the event
// actually is - low-stakes texture (a nice take, a minor scheduling
// wrinkle) vs a real, memorable turning point (a recast, a
// hospitalization). The engine rolls for severity before picking a
// template (engine/production.ts:rollDayEvent), skewed heavily toward
// `low` - most days that produce anything at all should produce something
// small, with `medium` and especially `high` genuinely rarer. See
// docs/DESIGN.md 5.21.
// Every template OWNS its finished-film consequence:
//  - `impact`: which department the event shapes (engine/productionExecution.ts).
//    The event definitions are the primary type system for this; the id-based
//    fallback in engine/productionExecution.ts:classifyEventImpact is only for
//    an event that somehow reaches scoring without one. For the risk-dimension
//    bank below, impact is assigned per dimension by tagImpact() (a morale
//    event shapes performances, a technical event shapes visual, ...), which a
//    template may still override inline.
//  - `escalates`: 0..1, how much a *negative* event pressures the rest of the
//    shoot - the seed of a bounded failure chain (engine/production.ts:
//    computeShootEscalation). Omitted => a sensible default from severity
//    (high 0.6, medium 0.25, low 0); set it explicitly to deviate (e.g. a
//    contained mishap that shouldn't cascade, or a positive event, gets 0).
interface SimpleProductionEventTemplate {
  id: string;
  description: string;
  polarity: 'positive' | 'negative';
  severity: EventSeverity;
  interactive?: false;
  costRange: [number, number];
  qualityRange: [number, number];
  buzzRange: [number, number];
  delayDaysRange: [number, number];
  impact?: ProductionExecutionImpact;
  escalates?: number;
}

// An event that pauses photography and hands the player a real decision,
// instead of auto-resolving - see types/index.ts:PendingEventChoice and
// state/studioReducer.ts:RESOLVE_EVENT_CHOICE. Each choice rolls its own
// outcome independently - there's no base cost/quality/buzz/delay range on
// the template itself, since the player's pick decides which of those
// actually applies, not a shared roll underneath every option. Which
// resources a given choice touches is whatever the situation actually
// implies: "pay a mediator" is pure cost because that's genuinely all it
// costs, but "replace someone mid-shoot" is cost *and* delay, because
// severance and onboarding a replacement are both real. Don't force a
// choice down to one resource for its own sake - the combination should
// read as obviously true of the scenario, not artificially simplified.
interface InteractiveProductionEventTemplate {
  id: string;
  situation: string; // may contain a "{name}" token - interpolated with the involved talent's name at roll time
  polarity: 'positive' | 'negative';
  severity: EventSeverity;
  interactive: true;
  choices: EventChoiceTemplate[]; // label/description may also contain "{name}"
  // Ties this event to whoever's actually hired for that role
  // (FilmDraft.talent), so the situation and choices can name them directly
  // and any `skillSensitive` choice can be adjusted by their actual skill
  // (or, for actors, their compatibility with the script) - see
  // engine/production.ts:rollDayEvent. Only roles guaranteed hired by the
  // time photography starts (every MANDATORY_TALENT_ROLES entry) are safe
  // to use here.
  involvesRole?: ProductionRole;
  // If set (requires involvesRole), two real recast candidates are pulled
  // from the studio's talent pool near the departing person's salary and
  // appended to `choices` at roll time (engine/production.ts:rollDayEvent) -
  // an actual replacement decision, not a flavor option, with cost and
  // quality driven by which specific person the player picks. Always
  // `high` severity - recasting mid-shoot is a genuinely big deal.
  offersReplacementFor?: ProductionRole;
  impact?: ProductionExecutionImpact;
  escalates?: number;
}

export type ProductionEventTemplate = SimpleProductionEventTemplate | InteractiveProductionEventTemplate;

/** Assigns a default `impact` (department) to every template in a bank; an inline `impact` on a template still wins. */
function tagImpact(impact: ProductionExecutionImpact, templates: ProductionEventTemplate[]): ProductionEventTemplate[] {
  return templates.map((t) => ({ impact, ...t }));
}

