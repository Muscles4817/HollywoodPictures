import type { CharacterArchetype, CharacterProminence, Script, ScriptCharacter } from '../../types';
import { estimateScriptCost } from '../../engine/scriptGenerator';
import { CHARACTER_ARCHETYPE_PROFILES } from '../characterArchetypes';

// Ten real films, hand-authored rather than procedurally generated - for
// the Recommendation Inspector only (components/dev/RecommendationInspector.tsx).
// The point is calibration: it's much easier to judge "does this
// recommendation feel right" against a script you already have a strong,
// specific intuition for (Mad Max IS practical-heavy; Gravity IS
// almost entirely digital) than against an anonymous procedurally
// generated one. Every field here (including archetype/storyType/setting/
// scale/productionRequirements) is a deliberate judgment call about each
// film's real production, not derived from data/scriptArchetypes.ts the way
// engine/scriptGenerator.ts's real generation is - that's the whole point,
// since a data-driven default can't know that Jaws (Thriller) is far more
// practical/location-heavy than a typical Thriller, or that Gravity
// (Sci-Fi) is far more digital than a typical Sci-Fi.
// Dev-only reference data - never imported by anything the player's own
// save touches.

function script(fields: Omit<Script, 'id' | 'cost'> & { id: string }): Script {
  return { ...fields, cost: estimateScriptCost(fields) };
}

// Same hand-picked, baseTraits-verbatim approach as data/testScripts.ts -
// see that file's own comment. Kept independent on purpose (this file's own
// header comment already explains why the two never share code).
function character(name: string, archetype: CharacterArchetype, prominence: CharacterProminence): ScriptCharacter {
  const id = name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
  return { id, name, archetype, prominence, traits: { ...CHARACTER_ARCHETYPE_PROFILES[archetype].baseTraits } };
}

