import type { Asset, CharacterArchetype, CharacterProminence, Script, ScriptCharacter } from '../types';
import { estimateScriptCost } from '../engine/scriptGenerator';
import { CHARACTER_ARCHETYPE_PROFILES } from './characterArchetypes';

// Eighty-eight real films, hand-authored rather than procedurally generated -
// eleven per Genre, permanently visible in the Asset Library's "Test Scripts"
// section so a player can develop something with a concrete, well-known
// identity (cast expectations, tone, scale) without needing to acquire a
// real Opportunity first. Every field here (including archetype/storyType/
// setting/scale/productionRequirements) is a deliberate judgment call about
// each film's real production, not derived from data/scriptArchetypes.ts
// the way engine/scriptGenerator.ts's real generation is - the same
// philosophy src/data/dev/referenceScripts.ts already established for the
// (separate, dev-only) Recommendation Inspector calibration set. This file
// is independent of that one on purpose: that one is never imported by
// anything the player's own save touches, and this one is the opposite -
// entirely player-facing - so the two deliberately don't share code, even
// where they cover the same film (Fury Road/Grand Budapest/12 Angry Men/
// Blair Witch/Before Sunrise/The Matrix/Gravity/Fellowship/Jaws all appear
// in both, independently authored).
//
// Seeded into every fresh Studio's assets by state/persistence.ts:loadState
// and the RESET_SAVE reducer case (state/studioReducer.ts) - not by
// createInitialStudio itself, so test fixtures that build a Studio directly
// for unit tests keep getting a genuinely empty assets: [] baseline.
// acquisitionCost is 0 (free, no Opportunity was ever "bought") and
// acquiredOnDay is 1 (present since the studio's founding). Once turned
// into a Project, a test script behaves exactly like any other owned
// Asset - it becomes "in development" then "used" via the same
// engine/project.ts:deriveAssetStatus every real asset already goes
// through, nothing special-cased.

function script(fields: Omit<Script, 'id' | 'cost'> & { id: string }): Script {
  return { ...fields, cost: estimateScriptCost(fields) };
}

function testAsset(s: Script): Asset {
  return { id: s.id, script: s, source: 'Studio Original', acquisitionCost: 0, acquiredOnDay: 1 };
}

// Hand-picked, not procedurally generated - same judgment-call philosophy as
// every other field in this file. baseTraits are taken verbatim from each
// Character Archetype's own profile (data/characterArchetypes.ts) rather
// than hand-tuned per character, since these are illustrative real-film
// casts, not calibration fixtures.
function character(name: string, archetype: CharacterArchetype, prominence: CharacterProminence): ScriptCharacter {
  const id = name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
  return { id, name, archetype, prominence, traits: { ...CHARACTER_ARCHETYPE_PROFILES[archetype].baseTraits } };
}

